import mongoose from "mongoose";

const expensesSchema = new mongoose.Schema(
  {
    spend: {
      type: Number,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  { timestamps: true },
);

export default mongoose.model("Expense", expensesSchema);
